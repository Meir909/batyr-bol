        for i, m in enumerate(u["daily_missions"]):
            if i in u["done"]:
                continue  # Skip completed missions
                
            correct = m["answers"]
            # For open-ended questions (no specific answers)
            if correct is None:
                mission = m
                num = i
                break
            # For specific answer questions
            elif any(c in user_answer for c in correct):
                mission = m
                num = i
                break
        
        if mission is None:
            await update.message.reply_text("‚ùå –ñ–∞—É–∞–ø –¥“±—Ä—ã—Å –µ–º–µ—Å –Ω–µ–º–µ—Å–µ –±“±–ª –º–∏—Å—Å–∏—è –æ—Ä—ã–Ω–¥–∞–ª“ì–∞–Ω. /missions –∞—Ä“õ—ã–ª—ã —Ç–µ–∫—Å–µ—Ä—ñ“£—ñ–∑")
